int main() {
    
    char a = '0';
    short b = 1;
    int c = 3;
    long d = 4; 
    double e = 5.;

    printf("a = %c\n", a);
    printf("b = %d\n", b);
    printf("c = %d\n", c);
    printf("d = %d\n", d);
    printf("e = %lf\n\n", e);

    return 0;
}


/*
prinft
%c -> caracter simples (char)
%d -> decimal (INT)
%ld -> inteiro “longo” (long int)
%f -> ponto flutuante (float)
%s -> cadeia de caracteres (string)
%lf -> double
*/
